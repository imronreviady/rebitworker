var DatatableDataLocalDemo = function () {
	//== Private functions

	// demo initializer
	var demo = function () {

		var dataJSONArray = JSON.parse('<?= $author_info; ?>');

		var datatable = $('.m_datatable').mDatatable({
			// datasource definition
			data: {
				type: 'local',
				source: dataJSONArray,
				pageSize: 10
			},

			// layout definition
			layout: {
				theme: 'default', // datatable theme
				class: '', // custom wrapper class
				scroll: false, // enable/disable datatable scroll both horizontal and vertical when needed.
				height: 450, // datatable's body's fixed height
				footer: false // display/hide footer
			},

			// column sorting(refer to Kendo UI)
			sortable: true,

			// column based filtering(refer to Kendo UI)
			filterable: false,

			pagination: true,

			// inline and bactch editing(cooming soon)
			// editable: false,
			// columns definition
			columns: [{
				title: "#",
				sortable: false, // disable sort for this column
				width: 40,
				textAlign: 'center',
				selector: {class: 'm-checkbox--solid m-checkbox--brand'}
			}, {
				field: "author_id",
				title: "Author ID"
			}, {
				field: "name",
				title: "Name",
				responsive: {visible: 'lg'}
			}, {
				field: "username",
				title: "Username"
			}, {
				field: "email",
				title: "Email",
				responsive: {visible: 'lg'}
			},  {
				field: "is_active",
				title: "Status",
				// callback function support for column rendering
				template: function (row) {
					var status = {
						true: {'title': 'Active', 'class': ' m-badge--success'},
						false: {'title': 'In Active', 'class': ' m-badge--danger'}
					};
					return '<span class="m-badge ' + status[row.is_active].class + ' m-badge--wide">' + status[row.is_active].title + '</span>';
				}
			}, {
				field: "is_logged_in",
				title: "Available",
				// callback function support for column rendering
				template: function (row) {
					var status = {
						false: {'title': 'Offline', 'state': 'danger'},
						true: {'title': 'Online', 'state': 'primary'} 
					};
					return '<span class="m-badge m-badge--' + status[row.is_logged_in].state + ' m-badge--dot"></span>&nbsp;<span class="m--font-bold m--font-' + status[row.is_logged_in].state + '">' + status[row.is_logged_in].title + '</span>';
				}
			}, {
				field: "Actions",
				width: 110,
				title: "Actions",
				sortable: false,
				overflow: 'visible',
				template: function (row) {
					var dropup = (row.getDatatable().getPageSize() - row.getIndex()) <= 4 ? 'dropup' : '';

					var active_or_not = {
						true: {'link': '#', 'icon': 'la-close', 'title': 'Deactivate'},
						false: {'link': '#', 'icon': 'la-check-circle-o', 'title': 'Activate'}
					};

					return '\
						<div class="dropdown '+ dropup +'">\
							<a href="#" class="btn m-btn m-btn--hover-accent m-btn--icon m-btn--icon-only m-btn--pill" data-toggle="dropdown">\
                                <i class="la la-ellipsis-h"></i>\
                            </a>\
						  	<div class="dropdown-menu dropdown-menu-right">\
						    	<a class="dropdown-item" href="' + active_or_not[row.is_active].link + '"><i class="la ' + active_or_not[row.is_active].icon + '"></i> ' + active_or_not[row.is_active].title + '</a>\
						    	<a class="dropdown-item" href="#"><i class="la la-eye"></i> Details</a>\
						    	<a class="dropdown-item" href="<?= base_url(); ?>admin/authors/delete/' + row.author_id + '"><i class="la la-trash"></i> Delete</a>\
						  	</div>\
						</div>\
						<a href="#" class="m-portlet__nav-link btn m-btn m-btn--hover-accent m-btn--icon m-btn--icon-only m-btn--pill" title="View ">\
                            <i class="la la-edit"></i>\
                        </a>\
					';
				}
			}]
		});

		var query = datatable.getDataSourceQuery();

		$('#m_form_search').on('keyup', function (e) {
			datatable.search($(this).val().toLowerCase());
		}).val(query.generalSearch);

		$('#m_form_status').on('change', function () {
			datatable.search($(this).val(), 'is_active');
		}).val(typeof query.is_active !== 'undefined' ? query.is_active : '');

		$('#m_form_type').on('change', function () {
			datatable.search($(this).val(), 'is_logged_in');
		}).val(typeof query.is_logged_in !== 'undefined' ? query.is_logged_in : '');

		$('#m_form_status, #m_form_type').selectpicker();

	};

	return {
		//== Public functions
		init: function () {
			// init dmeo
			demo();
		}
	};
}();

jQuery(document).ready(function () {
	DatatableDataLocalDemo.init();
